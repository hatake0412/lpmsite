      <p>Download LPM by wget or any browser, add an execution permission to the downloaded file, type 'lpm initlocaldir'. Then everything runs automatically. Here is an example:</p>
      <p class="ex">$ wget http://www.kasahara.ws/lpm/lpm<br />$ chmod +x ./lpm<br />$ ./lpm initlocaldir<br /></p>
      <p>After 'lpm initlocaldir', LPM is copied into ~/lcl/bin/lpm, so the downloaded LPM can be safely removed.</p>
      <p class="ex">$ rm lpm</p>
